package testi1;

class WordPaira {
	   private String englishWord;
	    String finnishWord;

	    WordPaira(String englishWord, String finnishWord) {
	        this.englishWord = englishWord;
	        this.finnishWord = finnishWord;
	    }

	    String getEnglishWord() {
	        return englishWord;
	    }

	    String getFinnishWord() {
	        return finnishWord;
	    }
	}

